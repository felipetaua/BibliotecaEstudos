/*
    Math

    Math é um objeto global em JavaScript que oferece funções e constantes matemáticas.

    Entre eles existem várias funcionalidades:
    
    - Arredondamentos.
    - Operações trigonométricas.
    - Logaritmos.
    - Aleatoriedade.
    - Potências.
    - Máximos e mínimos.
    - Raízes quadradas.
*/

console.log(Math.PI + 2)// mostra o valor do PI
console.log(Math.pow(2,3)) // permite usarmos potenciaciação (primeiro numbero base, segundo qual valor vai elevar)
console.log(Math.sqrt(25,2)) // permite calcular a raiz quadrada de um numero
console.log(Math.random()) // Mostra um numero aleatorio entre 0 e 1
console.log(Math.ceil(23.6)) // vai arredondar um numero para "CIMA"
console.log(Math.floor(23.6)) // vai arredondar um numero para "BAIXO"