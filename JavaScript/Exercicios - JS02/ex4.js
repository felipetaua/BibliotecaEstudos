// Faça um programa que compare dois números e diga qual é o maior, ou se são iguais.

/* 
    pensamentos
    == 
    value1 > value2  value1 < value2 || value1 == value2 
 */

let value1 = 10
let value2 = 20

if (value1 == value2) {
    console.log(`Os valores ${value1} e ${value2} são iguais`)
} if( value1 > value2) {
    console.log(`O valor1: ${value1} é maior que valor2: ${value2}`)
} else {
    console.log(`O valor2: ${value2} é maior que valor1: ${value1}`)
}


